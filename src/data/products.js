const pendingStatus = {
  coaStatus: "COA Pending",
  batchStatus: "Batch Pending",
  qrStatus: "QR Pending",
};

const researchMetadata = {
  researchUseOnly: true,
  researchNotice:
    "For Research Use Only. Not intended for human consumption.",
};

function variant(
  strength,
  codeName,
  price,
  image = null,
  composition = "",
  options = {}
) {
  return {
    ...pendingStatus,
    ...researchMetadata,
    strength,
    codeName,
    price,
    image,
    ...(composition
      ? { composition }
      : {}),
    ...options,
  };
}

function product({
  name,
  codeName,
  strength,
  category,
  description,
  isBestSeller = false,
  purity = "≥ 99% Purity",
  variants = [],
  options = {},
}) {
  const firstVariant =
    variants[0] || null;

  const hasSingleVariant =
    variants.length === 1;

  const productData = {
    ...pendingStatus,
    ...researchMetadata,
    name,

    image:
      firstVariant?.image || null,

    codeName: hasSingleVariant
      ? firstVariant.codeName
      : codeName,

    strength: hasSingleVariant
      ? firstVariant.strength
      : strength,

    category,
    purity,

    price:
      firstVariant?.price ?? null,

    description,
    isBestSeller,
    ...options,
  };

  if (hasSingleVariant) {
    return {
      ...productData,
      ...firstVariant,
      name,
      category,
      purity,
      description,
      isBestSeller,
      ...options,
    };
  }

  return {
    ...productData,

    ...(variants.length > 1
      ? { variants }
      : {}),
  };
}

